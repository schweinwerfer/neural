package de.ora.neural.core.net;

/**
 * Operations for {@link GenericMatrix} fingerprints
 */
public enum FPOps {
    /**
     * No operation
     */
    NOP,
    /**
     * Rotate
     */
    ROT,
    /**
     * Mirror X
     */
    MX,
    /**
     * Mirror Y
     */
    MY,
    /**
     * Mirror diagonal 1
     */
    D1;
}
